    // Get icon class based on notification type
    public function getIconClass()
    {
        return match($this->type) {
            self::BOOKING_CREATED => 'text-blue-500',
            self::BOOKING_APPROVED => 'text-green-500',
            self::BOOKING_REJECTED => 'text-red-500',
            self::BOOKING_ASSIGNED => 'text-yellow-500',
